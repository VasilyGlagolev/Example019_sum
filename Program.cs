// Задача 36: Задайте одномерный массив, заполненный случайными числами. Найдите сумму элементов, стоящих на нечётных позициях.
// [3, 7, 23, 12] -> 19
// [-4, -6, 89, 6] -> 0

using System;

class Program
{
    static void Main(string[] args)
    {
        // Запрашиваем у пользователя размер массива
        Console.Write("Введите размер массива: ");
        int size = int.Parse(Console.ReadLine());

        // Создаем массив из случайных чисел
        int[] array = new int[size];
        Random random = new Random();
        for (int i = 0; i < array.Length; i++)
        {
            array[i] = random.Next(-100, 100);
        }

        // Выводим массив на экран
        Console.WriteLine("Массив:");
        foreach (int element in array)
        {
            Console.Write(element + " ");
        }
        Console.WriteLine();

        // Считаем сумму элементов, стоящих на нечетных позициях
        int sum = 0;
        for (int i = 1; i < array.Length; i += 2)
        {
            sum += array[i];
        }

        // Выводим результат на экран
        Console.WriteLine("Сумма элементов, стоящих на нечетных позициях: " + sum);

        // Ждем нажатия клавиши Enter перед закрытием окна консоли
        Console.ReadLine();
    }
}


// Программа сначала запрашивает у пользователя размер массива и создает массив из случайных чисел с помощью генератора
// случайных чисел. 
// Затем она выводит массив на экран и считает сумму элементов, стоящих на нечетных позициях в цикле, 
// перебирая элементы массива с шагом 2. 
// Наконец, программа выводит сумму на экран.
